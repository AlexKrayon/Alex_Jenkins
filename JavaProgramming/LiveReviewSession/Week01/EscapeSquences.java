package Week01;

public class EscapeSquences {
    public static void main(String[] args) {

        /*
        \t: tab
        \n: takes it to next line
        \\: prints \
        \": prints "

         */

        System.out.println("Learn\tJava\n\tthe\\Hard\tWay\n\n");
        System.out.println("\tLearn JAVA the \"EASY WAY\" WAY\n\twith Oscar");
    }


}
