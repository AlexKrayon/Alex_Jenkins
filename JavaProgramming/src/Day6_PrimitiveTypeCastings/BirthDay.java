package Day6_PrimitiveTypeCastings;

public class BirthDay {

    public static void main(String[] args) {
        /*
        name, birthDay(int), birthMonth(String), birthYear (int)
         */
        String name = "Yavuz";
        int birthDay = 02;
        String birthMonth = "February";
        int birthYear = 2009;

        System.out.println(name + " was born " + birthDay + " "+ birthMonth + " "+birthYear+".");

        System.out.println("-------------------------------------------------");
        // My favorite book is "nookName"
        String bookName = "Sefiller";
        System.out.println("My favorite book is "+ "\"" +bookName+"\"");




    }
}
