package week_3_ReviewSection;

public class AssignmentOperators {
    public static void main(String[] args) {
        int i = 100;
        i+=10;
        System.out.println("i = " + i);
        i-= 20;
        System.out.println("i = " + i);
        i*=3;
        System.out.println("i = " + i);
        i/= 5;
        System.out.println("i = " + i);
        i%= 4;
        System.out.println("i = " + i);
        
    }
}
